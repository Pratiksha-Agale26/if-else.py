num1=int(input("enter the number"))
num2=int(input("enter the numer"))
ope=input("enter the operater")
if ope=="+":
    print(num1+num2)
elif ope=="-":
    print(num1-num2)
elif ope=="*":
    print(num1*num2)
elif ope=="%":
    print(num1%num2)
elif ope=="/":
    print(num1/num2)
elif ope=="//":
    print(num1//num2)
else:
    print("nothing")